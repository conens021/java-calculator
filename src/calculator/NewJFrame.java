    private void powSignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_powSignActionPerformed
        if (jTextArea1.getText().equals("")) {
            return;
        }
        double res = Double.parseDouble(jTextArea1.getText());
        res = Math.pow(res, 2);
        jTextArea1.setText(String.valueOf(res));
    }//GEN-LAST:event_powSignActionPerformed
    private double calc() {
        double res = 0;
        switch (operation) {
            case '+':
                res = operant1 + operant2;
                break;
            case '-':
                res = operant1 - operant2;
                break;
            case '*':
                res = operant1 * operant2;
                break;
            case '/':
                jTextArea1.setText("Deljenje sa nulom nije dozvoljeno");
                res = operant1 / operant2;
                break;
        }
        return res;
    }

