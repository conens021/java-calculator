    private void leftZagradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leftZagradaActionPerformed

        str = jTextArea1.getText();
        if (str.equals(null) || str.equals("")) {
            return;
        }
        jTextArea1.setText(removeLastChar(str));

    }//GEN-LAST:event_leftZagradaActionPerformed
    private static String removeLastChar(String str) {
        return str.substring(0, str.length() - 1);
    }
